package java21;

public class Loop {
    public static void main(String[] args) {
        for (int i = 0; i < 2000; i++) {
            System.out.println(i+". Em hua lam bai tap day du");
        }
        System.out.println("\n-----\n");

        for (int i = 2000; i >0; i--) {
            System.out.println(i+". Em hua lam bai tap day du");
        }
    }
}
